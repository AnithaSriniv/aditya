package org.contr;

public class SwitchCase {
	public static void main(String[] args) {
		int i = 30;
		switch (i) {
		case 1:
			System.out.println("hi");
			break;
		case 2 :
			System.out.println("Ani");
			break;
		case 30:
			System.out.println("Adi");
			
		default:
			System.out.println("Part");
			break;
		}
	}

}
